    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int selectedRow = tblProduk.getSelectedRow();

if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Pilih dulu data yang ingin dihapus.");
    return;
}

// Ambil kode dari baris yang dipilih
String kode = tblProduk.getValueAt(selectedRow, 0).toString();

// Konfirmasi
int konfirmasi = JOptionPane.showConfirmDialog(
    this,
    "Yakin ingin menghapus produk dengan kode: " + kode + "?",
    "Konfirmasi Hapus",
    JOptionPane.YES_NO_OPTION
);

if (konfirmasi == JOptionPane.YES_OPTION) {
    try (Connection conn = KoneksiDB.getConnection()) {
        String sql = "DELETE FROM products WHERE kode = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, kode);
        int result = ps.executeUpdate();

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Produk berhasil dihapus.");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus produk.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    // Setelah delete, refresh tabel
    btnLoad.doClick(); // seolah klik tombol Load
}

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProduk.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu baris yang ingin diupdate");
            return;
        }
        String kode = tblProduk.getValueAt(selectedRow, 0).toString();
        String nama = tblProduk.getValueAt(selectedRow, 1).toString();
        String harga = tblProduk.getValueAt(selectedRow, 2).toString();
        String stok = tblProduk.getValueAt(selectedRow, 3).toString();
        
        new UpdateProduct(kode, nama, harga, stok).setVisible(true);
    }//GEN-LAST:event_btnUpdateActionPerformed

