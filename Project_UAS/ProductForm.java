    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
    DefaultTableModel model = new DefaultTableModel(
    new Object[]{"Kode", "Nama", "Harga", "Stock"},0 ) {
        
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // supaya tabel tidak bisa diedit
        }
    };
    tblProduk.setModel(model); //ini akan menimpa model dari desain
    tblProduk.getColumnModel().getColumn(3).setCellEditor(new DefaultCellEditor(new JTextField())); //hapus spinner/editor angka di stok

try (Connection conn = KoneksiDB.getConnection()) {
     String sql = "Select * FROM products";
     PreparedStatement ps = conn.prepareStatement(sql);
     ResultSet rs = ps.executeQuery();

    while (rs.next()) {
        Object[] row = {
            rs.getInt("kode"),
            rs.getString("nama"),
            rs.getInt("harga"),
            rs.getInt("stock")
        };
        model.addRow(row);
    }

} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
}

    }//GEN-LAST:event_btnLoadActionPerformed

